<?php
// Strings.
# Uma "String" é o conjunto concatenado de caracteres armazenados em uma variável.
# Podemos definir uma "String" como sendo um conjunto de letras, espaços, símbolos etc, 
# designados como texto.

# Podem ser delimitadas por:
# aspas simples('', chamada de simple quote) ou duplas("", chamada de double quote)

# Podemos usar uma ou outra, basta manter um padrão no código.

# Exemplo 1
$a = 'Esta é uma String';
$b = "Também sou uma String";

echo $a;
echo "<br>$b";

# Exemplo 2
$a = 'Esta é uma "String"';
$b = "Também sou uma 'String'";

echo "<br><br>$a";
echo "<br>$b";
?>