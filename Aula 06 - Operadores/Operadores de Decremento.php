<?php
// Operadores de Incremento e Decremento
// Permitem adicionar ou subtrair uma unidade à variável
//================

// DECREMENTO
$y = 5;
echo ($y);
echo ('<br>');

# Tradicional
$y =  5 - 1;
echo ($y);
echo ('<br>');

# Pós-Decremento
$y--;
echo ($y);
echo ('<br>');

# Pré-Decremento
--$y;
echo ($y);
echo ('<br>');
?>